/**
 * Grunt configuration
 */

module.exports = function(grunt) {

    // Import modules.
    let path = require('path');

    let cwd = process.env.PWD || process.cwd(),
        inAMD = path.basename(cwd) === 'amd';

    // Globbing pattern for matching all AMD JS source files.
    let amdSrc = [inAMD ? cwd + '/src/*.js' : '**/amd/src/*.js'];

    // PHP strings for exec task.
    let moodleroot = path.dirname(path.dirname(path.dirname(__dirname))),
        decachephp = '',
        dirrootopt = grunt.option('dirroot') || process.env.MOODLE_DIR || '';

    // Allow user to explicitly define Moodle root dir.
    if ('' !== dirrootopt) {
        moodleroot = path.resolve(dirrootopt);
    }

    let configfile = path.join(moodleroot, 'config.php');

    decachephp += 'define(\'CLI_SCRIPT\', true);';
    decachephp += 'require(\'' + configfile + '\');';
    decachephp += 'theme_reset_all_caches();';

    /**
     * Function to generate the destination for the uglify task
     * (e.g. build/file.min.js). This function will be passed to
     * the rename property of files array when building dynamically:
     * http://gruntjs.com/configuring-tasks#building-the-files-object-dynamically
     *
     * @param {String} destPath the current destination
     * @param {String} srcPath the  matched src path
     * @return {String} The rewritten destination path.
     */
    let uglifyRename = function(destPath, srcPath) {
        destPath = srcPath.replace('src', 'build');
        destPath = destPath.replace('.js', '.min.js');
        destPath = path.resolve(cwd, destPath);
        return destPath;
    };

    grunt.initConfig({
        uglify: {
            amd: {
                files: [{
                    expand: true,
                    src: amdSrc,
                    rename: uglifyRename
                }],
                options: {report: 'none'}
            }
        }
    });

    // Load contrib tasks.
    grunt.loadNpmTasks('grunt-contrib-uglify-es');

    // Register tasks.
    grunt.registerTask('amd', ['uglify']);

};
